#include <iostream>

using namespace std;
int main()
{
    // + , - , * , / , %
    int a = 3;
    int b = 2;

    int c = a + b;
    int d = a - b;
    int e = a * b;
    int f = a / b;
    int g = a % b;

    cout << c << endl;
    cout << d << endl;
    cout << e << endl;
    cout << f << endl;
    cout << g << endl;

    return 0;
}